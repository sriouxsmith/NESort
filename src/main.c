#include "nes.h"

const uint8_t palette[] = {
	0x0f, 0x00, 0x10, 0x30,
	0x0f, 0x29, 0x27, 0x30,
	0x0f, 0x05, 0x16, 0x27,
	0x0f, 0x0b, 0x1a, 0x29,

	0x0f, 0x00, 0x10, 0x30,
	0x0f, 0x29, 0x27, 0x30,
	0x0f, 0x05, 0x16, 0x27,
	0x0f, 0x0b, 0x1a, 0x29
};

void main(void) {
	nes_set_vram_update(32, palette, 0x3f00);
	nes_wait_frame();

	while (1) nes_wait_frame();
}
